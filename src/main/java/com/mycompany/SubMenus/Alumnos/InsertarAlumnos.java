    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        AlumnoDAO ad = new AlumnoDAO();
        String nombre = null, apellidoP = null, apellidoM = null;
        int year = 0, mes = 0, dia = 0;
        int seleccionYear = cbYear.getSelectedIndex(), seleccionMes = cbMes.getSelectedIndex(), seleccionDia = cbDia.getSelectedIndex();
        if (txtNombre.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar un nombre", "Nombre no ingresado", JOptionPane.ERROR_MESSAGE);
        } else {
            nombre = txtNombre.getText();
        }
        if (txtApellidoP.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar el apellido paterno", "Apellido paterno no ingresado", JOptionPane.ERROR_MESSAGE);

        } else {
            apellidoP = txtApellidoP.getText();
        }
        if (txtApellidoM.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar el apellido materno", "Apellido materno no ingresado", JOptionPane.ERROR_MESSAGE);

        } else {
            apellidoM = txtApellidoM.getText();
        }
        
        year = Integer.parseInt(cbYear.getItemAt(seleccionYear));
        year = year - 1900;
        mes = Integer.parseInt(cbMes.getItemAt(seleccionMes));
        dia = Integer.parseInt(cbDia.getItemAt(seleccionDia));
        Date d = new Date(year, mes, dia);
        //Insertando nuevo objeto del tipo alumno
        Alumno a = new Alumno(nombre, apellidoP, apellidoM, d);
        try {
            ad.insertar(a);
            JOptionPane.showMessageDialog(rootPane, "Alumno ingresado exitosamente", "Registro insertado", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(InsertarAlumnos.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnInsertarActionPerformed

