    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        DocenteDAO dd = new DocenteDAO();
        Docente d = new Docente();
        String nombre = "", apellidoP = "", apellidoM = "";
        int seleccionYear = 0, seleccionMes = 0, seleccionDia = 0;
        int year = 0, mes = 0, dia = 0, idDocente = 0;
        
        if(txtNombre.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "No se ingreso el nombre", "Informacion faltante", JOptionPane.ERROR_MESSAGE);
        }else{
            nombre = txtNombre.getText();
        }
        if(txtApellidoP.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "No se ingreso el apellido paterno", "Informacion faltante", JOptionPane.ERROR_MESSAGE);
        }else{
            apellidoP = txtApellidoP.getText();
        }
        if(txtApellidoM.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "No se ingreso el apellido materno", "Informacion faltante", JOptionPane.ERROR_MESSAGE);
        }else{
            apellidoM = txtApellidoM.getText();
        }
        if(txtIdDocente.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "No ingreso el ID del docente al que se le modificara la informacion", "Informacion faltante", JOptionPane.ERROR_MESSAGE);
        }else{
            idDocente = Integer.parseInt(txtIdDocente.getText());
        }
        
        seleccionYear = cbYear.getSelectedIndex();
        seleccionMes = cbMes.getSelectedIndex();
        seleccionDia = cbDia.getSelectedIndex();
        
        year = Integer.parseInt(cbYear.getItemAt(seleccionYear));
        year = year - 1900;
        mes = Integer.parseInt(cbMes.getItemAt(seleccionMes));
        dia = Integer.parseInt(cbDia.getItemAt(seleccionDia));
        
        Date DoB = new Date(year, mes, dia);
        d.setNombre(nombre);
        d.setApellidoP(apellidoP);
        d.setApellidoM(apellidoM);
        d.setDoB(DoB);
        d.setIdDocentes(idDocente);
        try {
            dd.actualizar(d);
            JOptionPane.showMessageDialog(rootPane, "Se ha actualizado la informacion del docente", "Registro actualizado", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(ActualizarDocentes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

